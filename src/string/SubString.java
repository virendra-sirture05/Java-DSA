package string;

public class SubString {
    public static void main(String[] args) {
        String str = "abcde";
        System.out.println(str.substring(1));
        System.out.println(str.substring(2,4));
        System.out.println(str.substring(2,2));
        System.out.println(str.substring(0, 5));

    }
}
